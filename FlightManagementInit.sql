CREATE SCHEMA FLIGHT_MANAGEMENT;

CREATE TABLE COUNTRY
(CName VARCHAR(100) NOT NULL,
CountryCode VARCHAR(3) NOT NULL,
PRIMARY KEY(CountryCode) );
CREATE TABLE AIRLINE
(AirlineCountry VARCHAR(3),
AirlineName VARCHAR(30) NOT NULL,
ICAOCode VARCHAR(3) NOT NULL,
FlightCount Int NOT NULL,
PRIMARY KEY(ICAOCode),
FOREIGN KEY(AirlineCountry) REFERENCES COUNTRY(CountryCode) ON DELETE CASCADE ON U
PDATE CASCADE);
CREATE TABLE PILOT
(PName VARCHAR(50) NOT NULL,
PActivity VARCHAR(50) NOT NULL,
SSN CHAR(9) NOT NULL,
BirthDate DATE,
LicenseNum INT NOT NULL,
EPTAgrade INT NOT NULL,
FExperience INT NOT NULL,
AirlineCode VARCHAR(3),
PRIMARY KEY(SSN),
FOREIGN KEY(AirlineCode) REFERENCES AIRLINE(ICAOCode) ON DELETE CASCADE ON UPDATE
CASCADE);
CREATE TABLE AIRPLANE
(AirplaneID VARCHAR(7) NOT NULL,
TypeCode VARCHAR(4) NOT NULL,
Model VARCHAR(50) NOT NULL,
Occupant INT,
AActivity TEXT,
AirlineCode VARCHAR(3),
PRIMARY KEY(AirplaneID),
FOREIGN KEY(AirlineCode) REFERENCES AIRLINE(ICAOCode) ON DELETE CASCADE ON UPDATE
CASCADE);
CREATE TABLE AIRPORT
(AirportIATACode VARCHAR(3) NOT NULL,
AirportName VARCHAR(100),
AirportCountry VARCHAR(3),
PRIMARY KEY(AirportIATACode),
FOREIGN KEY(AirportCountry) REFERENCES COUNTRY(CountryCode) ON DELETE CASCADE ON U
PDATE CASCADE );
CREATE TABLE AIRPORT_GATE
(Gate VARCHAR(5) NOT NULL,
Terminal INT,
AirportCode VARCHAR(3) NOT NULL,
PRIMARY KEY(Gate, AirportCode),
FOREIGN KEY(AirportCode) REFERENCES AIRPORT(AirportIATACode) ON DELETE CASCADE ON
UPDATE CASCADE );
CREATE TABLE FLIGHT
(FlightNumber VARCHAR(6) NOT NULL,
AirlineCode VARCHAR(3) NOT NULL,
DepartureTime DATETIME,
ArrivalTime DATETIME,
FGate VARCHAR(5) NOT NULL,
PilotSSN CHAR(9) NOT NULL,
AirplaneID VARCHAR(7) NOT NULL,
OIATACode VARCHAR(3) NOT NULL,
DIATACode VARCHAR(3) NOT NULL,
DvsI VARCHAR(20),
PRIMARY KEY(FlightNumber),
FOREIGN KEY(AirlineCode) REFERENCES AIRLINE(ICAOCode) ON DELETE CASCADE ON UPDATE
CASCADE ,
FOREIGN KEY(FGate) REFERENCES AIRPORT_GATE(Gate) ON DELETE CASCADE ON UPDATE CASCA
DE ,
FOREIGN KEY(PilotSSN) REFERENCES PILOT(SSN) ON DELETE CASCADE ON UPDATE CASCADE ,
FOREIGN KEY(AirplaneID) REFERENCES AIRPLANE(AirplaneID) ON DELETE CASCADE ON UPDAT
E CASCADE ,
FOREIGN KEY(OIATACode) REFERENCES AIRPORT(AirportIATACode) ON DELETE CASCADE ON UP
DATE CASCADE ,
FOREIGN KEY(DIATACode) REFERENCES AIRPORT(AirportIATACode) ON DELETE CASCADE ON UP
DATE CASCADE );
